let a = 10;
let b = 4;

console.log(a+b); // Addition
console.log(a*b); // Substraction
console.log(a%b); // Modulus
console.log(a**b); // power/Exponent
console.log(a%b); // Division

// increment operator
console.log(a++);
console.log(++a);

// Decrement
console.log(a--);
console.log(--a);

// comparision operator
console.log("a !== b", a!==b);